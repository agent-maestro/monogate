def section3_koch() -> None:
    print(DIVIDER)
    print("SECTION 3 — KOCH CURVE (Subdivision Fractal)")
    print(DIVIDER)
    koch = KochCurve()
    analysis = koch.eml_analysis()
    print(f"  EML per subdivision step: {analysis['eml_per_step']}")
    print(f"  EML attractor: {analysis['eml_attractor']}")
    print(f"  Hausdorff dim: {analysis['hausdorff_dim']:.4f} (= log4/log3)")
    print()

    base = [complex(0), complex(1, 0), complex(0.5, math.sqrt(3)/2), complex(0)]
    for n in range(1, 6):
        pts = koch.iterate(list(base), n_steps=n)
        n_segs = len(pts) - 1
        theory = 3 * (4**n)
        print(f"  Step {n}: {n_segs} segments (theory {theory})")
    print()
    print(f"  Insight: {analysis['insight']}")
    print()


