def section5_julia() -> None:
    print(DIVIDER)
    print("SECTION 5 — JULIA SETS (Same Map, Fractal Parameter Space)")
    print(DIVIDER)

    test_cs = [
        (complex(-0.7269, 0.1889), "douady_rabbit"),
        (complex(0.285, 0.01), "parabolic"),
        (complex(-2.0, 0.0), "outside_cantor"),
    ]

    mb_test = MandelbrotSet(max_iter=50)
    for c, name in test_cs:
        js = JuliaSet(c=c, max_iter=100)
        analysis = js.eml_analysis()
        in_mb = mb_test.escape_time(c) == 50
        print(f"  c={c} ({name}):")
        print(f"    In Mandelbrot set: {in_mb}")
        print(f"    Expected topology: {'Connected' if in_mb else 'Cantor dust'}")
        print(f"    EML/step: {analysis['eml_per_step']}")
    print()
    print("  Key insight: connected Julia (c in M) vs Cantor Julia (c outside M).")
    print("  Both are EML-2 per step. The TOPOLOGY changes, not the map complexity.")
    print()


