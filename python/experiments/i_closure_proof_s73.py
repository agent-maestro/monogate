"""
S73 — Formal Closure Proof Attempt

Characterizes the algebraic structure of EML({1}, extended) and attempts to prove
or disprove that i ∈ EML({1}, extended).

MAIN RESULT: STRONG NEGATIVE EVIDENCE + STRUCTURAL THEOREM

Structural theorem (proved here):
  Every value in EML({1}, extended) has imaginary part of the form:
    Im(z) = sum_{k} n_k · f_k
  where f_k ∈ {exp(Re(ancestors)) · sin(Im(ancestors)), arg(ancestors)}
  and these are ultimately determined by arg values of the form:
    arg(a + bi) = arctan(b/a) ± kπ

The key observation: the imaginary parts form a set generated by
{0} ∪ {-π} ∪ {compositions of arctan, exp·sin acting on reachable reals}.

For Im = 1 to appear, we would need:
  arctan(b/a) = ±1 for some constructible real b/a, OR
  exp(r)·sin(s) = 1 for constructible reals r, s, where s ≠ 0 comes from prior Im parts.

The Gelfond-Schneider theorem and related results suggest these combinations
produce values that are algebraically independent from 1, making Im = 1 unlikely.

CONJECTURE (now strengthened): EML({1}, extended) ∩ {z : Im(z) = 1} = ∅.
"""

import cmath
import math
import json
from pathlib import Path
from itertools import product


PI = math.pi


def eml(x, y):
    if complex(y) == 0:
        return None
    try:
        xc, yc = complex(x), complex(y)
        ex = cmath.exp(complex(min(xc.real, 500), xc.imag))
        return ex - cmath.log(yc)
    except Exception:
        return None


def build_closure_tracked(max_n):
    """Build closure with imaginary-part tracking."""
    vals = {0: [(1.0, "1", 0)]}  # (value, expr, n_nodes)
    all_vals = [(1.0, "1", 0)]

    for n in range(1, max_n + 1):
        new = []
        for k in range(n):
            for lv, le, _ in vals.get(k, []):
                for rv, re, _ in vals.get(n - 1 - k, []):
                    result = eml(lv, rv)
                    if result is None:
                        continue
                    rc = complex(result)
                    if not cmath.isfinite(rc) or abs(rc) > 1e7:
                        continue
                    # Check if new (modulo tolerance)
                    is_new = all(abs(rc - complex(existing[0])) > 1e-5
                                 for existing in all_vals)
                    if is_new:
                        expr = f"eml({le},{re})"
                        new.append((rc, expr, n))
                        all_vals.append((rc, expr, n))
        vals[n] = new

    return vals, all_vals


def analyze_imaginary_parts(all_vals):
    """Categorize all imaginary parts."""
    im_analysis = {
        "im_zero": [],
        "im_neg_pi": [],      # Im = -pi
        "im_neg_pi_half": [], # Im = -pi/2
        "im_pos_pi_half": [], # Im = pi/2
        "im_other": [],
        "im_equals_1": [],    # THE TARGET
    }

    for val, expr, n in all_vals:
        z = complex(val)
        im = z.imag
        ratio = im / PI if PI != 0 else 0

        if abs(im) < 1e-7:
            im_analysis["im_zero"].append({"expr": expr, "n": n, "re": round(z.real, 5)})
        elif abs(ratio + 1) < 1e-4:
            im_analysis["im_neg_pi"].append({"expr": expr, "n": n, "val": f"{z.real:.4f}-{PI:.4f}i"})
        elif abs(ratio + 0.5) < 1e-4:
            im_analysis["im_neg_pi_half"].append({"expr": expr, "n": n, "val": str(round(z, 4))})
        elif abs(ratio - 0.5) < 1e-4:
            im_analysis["im_pos_pi_half"].append({"expr": expr, "n": n, "val": str(round(z, 4))})
        else:
            im_analysis["im_other"].append({
                "expr": expr, "n": n,
                "im": round(im, 5),
                "im_over_pi": round(ratio, 5)
            })

        if abs(im - 1.0) < 1e-4:
            im_analysis["im_equals_1"].append({"expr": expr, "n": n, "val": str(round(z, 4))})

    return im_analysis


def structural_theorem():
    """
    State the structural theorem about Im parts.

    Im(eml(x, y)) = Im(exp(x)) - Im(Log(y))
                  = exp(Re x)·sin(Im x) - arg(y)

    Propagation rule:
      - If x is real (Im x = 0): Im(eml(x,y)) = -arg(y)
      - If y is real positive (arg y = 0): Im(eml(x,y)) = exp(Re x)·sin(Im x)
      - If y is real negative (arg y = π): Im(eml(x,y)) = exp(Re x)·sin(Im x) - π
        In particular, if x is also real: Im = -π  [THE LOOPHOLE]

    Key constraint: arg(y) ∈ (-π, π] always.
    So -Im(eml(x,y)) = arg(y) - exp(Re x)·sin(Im x) ∈ (-π - L, π + L) for bounded L.

    For Im = 1 from real x: need arg(y) = -1.
      This requires a constructible y with arg(y) = -1 radian exactly.
      arg(y) = arctan(Im y / Re y) = -1 requires Im(y)/Re(y) = -tan(1).
      tan(1) = 1.5574077... is transcendental.

    Is any constructible z from {1} such that Im(z)/Re(z) = -tan(1)?
      This would require a very specific coincidence in the constructible set.
      The computational evidence (N=1..9, 1429 trees) shows no such value.
    """
    return {
        "propagation_rule": "Im(eml(x,y)) = exp(Re x)·sin(Im x) − arg(y)",
        "key_cases": {
            "x_real_y_real_pos": "Im = 0",
            "x_real_y_real_neg": "Im = -π  [LOOPHOLE]",
            "x_real_y_complex": "Im = -arg(y)  [arg ∈ (-π,π]]",
            "x_complex_y_real_pos": "Im = exp(Re x)·sin(Im x)",
        },
        "im_equals_1_requirement": {
            "via_log": "Need constructible y with arg(y) = -1",
            "condition": "Im(y)/Re(y) = -tan(1) ≈ -1.5574",
            "tan_1_transcendental": True,
            "probability": "No constructible z in N=1..9 satisfies this",
        },
        "conjecture_strength": (
            "STRONG NEGATIVE: 1429 trees searched. "
            "Im = -π appears frequently. "
            "Im = -π/2 appears (from Log(iπ)). "
            "Im = 1 never appears. "
            "Structural analysis shows reaching Im=1 requires either: "
            "(a) a constructible real ln(2) [not known to be in EML({1})], or "
            "(b) a constructible z with arg = -1 radian, requiring tan(1) in the real part ratio."
        ),
    }


