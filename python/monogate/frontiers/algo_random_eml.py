# ---------------------------------------------------------------------------
# Martin-Löf Randomness
# ---------------------------------------------------------------------------

@dataclass
class MartinLofRandomness:
    """
    Martin-Löf randomness: a sequence ω is random iff it passes all
    effectively null (measure-zero) tests — equivalently, ω does not belong
    to any computably enumerable set of measure zero.

    EML perspective: a random sequence has no EML-finite generating function;
    its complexity is EML-∞.
    """

    @staticmethod
    def frequency_test(seq: list[int], expected: float = 0.5, tol: float = 0.1) -> dict:
        """Monobit frequency test — first test in NIST SP 800-22."""
        n = len(seq)
        ones = sum(seq)
        freq = ones / n
        passes = abs(freq - expected) < tol
        return {
            "test": "monobit_frequency",
            "n": n,
            "ones": ones,
            "frequency": freq,
            "expected": expected,
            "passes": passes,
        }

    @staticmethod
    def runs_test(seq: list[int]) -> dict:
        """Runs test — counts alternating runs."""
        n = len(seq)
        runs = 1 + sum(1 for i in range(1, n) if seq[i] != seq[i - 1])
        # expected runs ≈ 2n·p·(1-p) where p = freq of 1s
        p = sum(seq) / n
        expected_runs = 2 * n * p * (1 - p)
        ratio = runs / expected_runs if expected_runs > 0 else float("nan")
        return {
            "test": "runs",
            "n": n,
            "observed_runs": runs,
            "expected_runs": round(expected_runs, 2),
            "ratio": round(ratio, 4),
            "passes": 0.8 < ratio < 1.2,
        }

    @staticmethod
    def block_entropy_test(seq: list[int], block_size: int = 8) -> dict:
        """Block entropy test — measure entropy of non-overlapping blocks."""
        n = len(seq)
        blocks = {}
        for i in range(0, n - block_size + 1, block_size):
            b = tuple(seq[i : i + block_size])
            blocks[b] = blocks.get(b, 0) + 1
        total = sum(blocks.values())
        entropy = -sum((c / total) * math.log2(c / total) for c in blocks.values() if c > 0)
        max_entropy = block_size  # bits
        ratio = entropy / max_entropy
        return {
            "test": "block_entropy",
            "block_size": block_size,
            "unique_blocks": len(blocks),
            "entropy_bits": round(entropy, 4),
            "max_entropy_bits": max_entropy,
            "ratio": round(ratio, 4),
            "passes": ratio > 0.9,
        }

    @staticmethod
    def classify_sequence(seq: list[int]) -> EMLClass:
        """
        Classify a binary sequence by its EML depth.
        - If generated by a known EML-finite formula → that depth
        - If passes all randomness tests → EML-∞ (empirically)
        """
        freq = MartinLofRandomness.frequency_test(seq)
        runs = MartinLofRandomness.runs_test(seq)
        blk = MartinLofRandomness.block_entropy_test(seq)
        n_pass = sum([freq["passes"], runs["passes"], blk["passes"]])
        if n_pass == 3:
            return EMLClass(EML_INF, "empirically random", "Passes ML randomness tests → no EML-finite generator")
        return EMLClass(2, "structured", "Fails randomness tests → EML-finite structure detectable")


