@dataclass
class NormalNumber:
    """
    A number α is normal in base b iff for every finite string s of length k,
    the frequency of s in the base-b expansion of α approaches b^{-k}.

    EML classification:
    - Normal numbers: EML-∞ (frequency function has dense zeros)
    - Borel's theorem: almost all reals are normal → EML-∞ a.e.
    - Computable normal numbers exist (Champernowne, Copeland-Erdős)
      but their digit functions are EML-2
    """

    name: str
    base: int
    is_normal: bool
    is_computable: bool
    eml_depth: float
    construction: str

    def eml_class(self) -> EMLClass:
        if self.eml_depth == EML_INF:
            return EMLClass(EML_INF, self.name, "Infinite zeros in frequency deviation function → EML-∞")
        return EMLClass(self.eml_depth, self.name, "Computable normal number via explicit construction")

    def frequency_deviation(self, digits: list[int], digit: int) -> list[float]:
        """
        Compute running frequency deviation of `digit` from expected 1/base.
        Returns list of deviations — for a truly normal number this oscillates
        infinitely → EML-∞ behavior.
        """
        expected = 1.0 / self.base
        deviations = []
        count = 0
        for i, d in enumerate(digits):
            if d == digit:
                count += 1
            freq = count / (i + 1)
            deviations.append(freq - expected)
        return deviations

    def zero_crossings(self, digits: list[int], digit: int) -> int:
        """Count zero crossings of frequency deviation — proxy for EML-∞."""
        devs = self.frequency_deviation(digits, digit)
        crossings = sum(1 for i in range(1, len(devs)) if devs[i - 1] * devs[i] < 0)
        return crossings

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "base": self.base,
            "is_normal": self.is_normal,
            "is_computable": self.is_computable,
            "eml_class": str(self.eml_class()),
            "eml_depth": self.eml_depth if self.eml_depth != EML_INF else "inf",
            "construction": self.construction,
        }


