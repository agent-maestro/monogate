@dataclass
class ComputableReal:
    """A real number defined by a computable digit-expansion function."""
    name: str
    description: str
    eml_class: EMLClass
    generating_formula: str
    digit_fn: Optional[Callable[[int], int]] = field(default=None, repr=False)

    def digits(self, n: int, base: int = 10) -> list[int]:
        """Return first n digits in given base."""
        if self.digit_fn is not None:
            return [self.digit_fn(k) for k in range(n)]
        return []

    def to_dict(self) -> dict:
        return {
            "name": self.name,
            "description": self.description,
            "eml_class": str(self.eml_class),
            "eml_depth": self.eml_class.depth if self.eml_class.depth != EML_INF else "inf",
            "generating_formula": self.generating_formula,
        }


def _pi_digit(n: int) -> int:
    """n-th decimal digit of pi (naive, slow — illustrative only)."""
    # Bailey–Borwein–Plouffe for hex digits; here we just use math
    val = math.pi
    s = f"{val:.50f}".replace(".", "")
    return int(s[n]) if n < len(s) else 0


def _e_digit(n: int) -> int:
    val = math.e
    s = f"{val:.50f}".replace(".", "")
    return int(s[n]) if n < len(s) else 0


def _sqrt2_digit(n: int) -> int:
    val = math.sqrt(2)
    s = f"{val:.50f}".replace(".", "")
    return int(s[n]) if n < len(s) else 0


def _champernowne_digit(n: int) -> int:
    """0.123456789101112... concatenation of naturals."""
    s = "".join(str(k) for k in range(1, 1000))
    return int(s[n]) if n < len(s) else 0


