EML_INF = float("inf")


@dataclass
class EMLClass:
    depth: float  # 0, 1, 2, 3, or inf
    label: str
    reason: str

    def __str__(self) -> str:
        d = "∞" if self.depth == EML_INF else str(int(self.depth))
        return f"EML-{d}: {self.label}"


