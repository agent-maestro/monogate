@dataclass
class ChaitinOmega:
    """
    Chaitin's Omega Ω = Σ_{p halts} 2^{-|p|}

    Properties:
    - 0 < Ω < 1
    - Ω is left-computable (can approximate from below)
    - Ω is not right-computable (halting problem)
    - Ω is Martin-Löf random
    - EML classification: EML-∞ (no EML-finite expression for its digits)

    We can compute lower bounds Ω_n using all programs of length ≤ n
    that halt within T steps.
    """

    @staticmethod
    def lower_bound(max_prog_len: int = 6, max_steps: int = 100) -> dict:
        """
        Compute a lower bound on Ω by enumerating short programs
        (represented as integers, simulated as counter machines).
        """
        # We model "programs" as integers p and "halting" as p being
        # a sum of distinct powers: if binary(p) has even weight → halts
        # This is a toy model preserving the probabilistic structure.
        total = Fraction(0)
        halting = []
        for p in range(1, 2 ** max_prog_len):
            bits = bin(p).count("1")
            prog_len = p.bit_length()
            # Toy halting criterion: halts iff popcount(p) is even
            if bits % 2 == 0:
                total += Fraction(1, 2 ** prog_len)
                halting.append(p)
        return {
            "max_prog_len": max_prog_len,
            "halting_programs": len(halting),
            "omega_lower_bound": float(total),
            "omega_lower_bound_exact": str(total),
            "eml_class": "EML-∞",
            "reason": "No EML-finite formula for Omega's digits; left-computable but not right-computable",
        }

    @staticmethod
    def eml_classification() -> EMLClass:
        return EMLClass(
            EML_INF,
            "Chaitin Ω",
            "Ω is Martin-Löf random → no EML-finite generating function. "
            "Any truncation Ω_n ≈ EML-2 but the limit is EML-∞.",
        )


