@dataclass
class EMLComputabilityTheorem:
    """
    Theorem: A real number α is computable iff there exists k < ∞ such that
    the digit-expansion function n ↦ d_n(α) is EML-k as a function on ℤ≥0.

    Equivalently:
    - Computable real ↔ EML-finite digit function
    - Martin-Löf random ↔ EML-∞ digit function (no finite depth suffices)
    - The EML depth of α = min k such that digits are EML-k computable

    This bridges computability theory (Turing, Church) with EML complexity.
    """
    statement: str = (
        "A real α is computable iff its digit function n↦d_n(α) is EML-finite. "
        "α is Martin-Löf random iff its digit function is EML-∞."
    )

    proof_sketch: str = (
        "Forward: if α computable, digit algorithm is a finite program → "
        "can be expressed as EML tree of bounded depth (each arithmetic op is EML-1 or EML-2). "
        "Backward: if digit function is EML-k for some k, then each digit d_n = "
        "round(EML_tree(n) mod base) is computable from n → α is computable. "
        "For Martin-Löf random α: any EML-finite f would give a computable approximation, "
        "but ML-random sequences are not computable → contradiction → must be EML-∞."
    )

    examples: list[dict] = field(default_factory=lambda: [
        {"real": "e", "eml_depth": 1, "why": "e = exp(1), direct EML-1 atom"},
        {"real": "ln 2", "eml_depth": 2, "why": "ln(2) = EML-2 (ln gate on integer)"},
        {"real": "√2", "eml_depth": 2, "why": "exp(½·ln 2) = EML-2"},
        {"real": "π", "eml_depth": 3, "why": "arctan via BBP → EML-3 oscillatory series"},
        {"real": "Champernowne", "eml_depth": 2, "why": "digit(n) = rational arithmetic → EML-2"},
        {"real": "Chaitin Ω", "eml_depth": "∞", "why": "ML-random, no computable digit extraction"},
        {"real": "generic Borel normal", "eml_depth": "∞", "why": "Almost all reals → EML-∞"},
    ])

    def to_dict(self) -> dict:
        return {
            "theorem": "EML Computability Theorem",
            "statement": self.statement,
            "proof_sketch": self.proof_sketch,
            "examples": self.examples,
        }


