@dataclass
class PrefixFreeKolmogorov:
    """
    Prefix-free Kolmogorov complexity K(x) = length of shortest self-delimiting program for x.

    K vs C: K(x) = C(x) + O(log C(x)) — close but K has cleaner coding theorem.

    Key results:
    - K(x) ≥ |x| - O(1) for most strings (incompressibility)
    - K(x|y) = K(x) + O(K(y)) (chain rule for K)
    - 2^{-K(x)} ≤ m(x) ≤ 2^{-C(x)} where m = universal semimeasure

    EML connection:
    - K(x) for a string x of n bits: K(x) ≈ n for random x (EML-∞)
    - K(x) ≈ k·log n for x generated by EML-k process (k = EML depth)
    - K(x) = O(1) for constant/periodic x (EML-0)
    """

    @staticmethod
    def k_upper_bounds() -> list[dict]:
        """Upper bounds on K for known classes."""
        return [
            {"class": "EML-0 (constant/periodic)", "K_upper": "O(1)", "eml_depth": 0},
            {"class": "EML-1 (exp(r·n) digits)", "K_upper": "O(log n)", "eml_depth": 1},
            {"class": "EML-2 (polynomial/log, e.g. Champernowne)", "K_upper": "O(log² n)", "eml_depth": 2},
            {"class": "EML-3 (π digits via BBP)", "K_upper": "O(log³ n)", "eml_depth": 3},
            {"class": "EML-∞ (ML-random)", "K_upper": "Θ(n) — incompressible", "eml_depth": "∞"},
        ]

    @staticmethod
    def coding_theorem() -> dict:
        return {
            "theorem": "Coding Theorem (Levin-Schnorr)",
            "statement": "m(x) = 2^{-K(x)} up to multiplicative constant, where m is the universal semimeasure",
            "eml_interpretation": "m(x) = EML-1 atom in K(x): m = exp(-K(x)·ln 2); K = EML-∞ for random x means m→0",
        }

    @staticmethod
    def randomness_deficiency(K_n: float, n: int) -> float:
        """d(x) = n - K(x): randomness deficiency (negative = random, large = structured)."""
        return n - K_n

    def to_dict(self) -> dict:
        return {
            "upper_bounds": self.k_upper_bounds(),
            "coding_theorem": self.coding_theorem(),
            "eml_k_correspondence": "EML-k process generates strings with K(x_n) = O(k·polylog(n))",
        }


@dataclass
class SolovayTest:
    """
    Solovay randomness: x is Solovay random iff Σ_n 2^{-K(x_n)} < ∞ along the
    sequence of initial segments x_n = x[0..n-1].

    EML interpretation:
    - 2^{-K(x_n)} = m(x_n) = EML-1 term in K
    - Σ < ∞ iff K(x_n) ≥ n - O(1) for all but finitely many n
    - This is equivalent to ML-randomness (Solovay ↔ ML)
    - The series Σ 2^{-K(x_n)} = EML-∞ in behavior (no EML-finite bound)
    """

    @staticmethod
    def series_bound(K_sequence: list[float]) -> dict:
        """Compute Σ 2^{-K(x_n)} for a given sequence of K values."""
        total = sum(2 ** (-k) for k in K_sequence if k < 1000)
        converges = total < float("inf")
        return {
            "K_sequence_sample": K_sequence[:10],
            "partial_sum": round(total, 6),
            "converges": converges,
            "random_by_solovay": converges,
        }

    def to_dict(self) -> dict:
        K_random = [n - 0.5 for n in range(1, 20)]  # K(x_n) ≈ n (random)
        K_structured = [math.log2(n + 1) * 3 for n in range(1, 20)]  # K ≈ 3log n (EML-2)
        return {
            "theorem": "Solovay ↔ Martin-Löf (both EML-∞)",
            "random_series": self.series_bound(K_random),
            "structured_series": self.series_bound(K_structured),
            "eml_solovay": "The Solovay sum Σ 2^{-K(x_n)} is EML-1 per term; convergence = EML-∞ of x",
        }


